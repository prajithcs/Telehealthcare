package com.iorbit.telehealthcare.core.data;

public class Device {
	
	private String name;
	private String ID;
	private String type;
	private int port;
	private String authenticationDetails;
	private String description;
	private String manufacturer;
	private String version;
	private String protocol;
	private String parameters;
	private String registrationFor;
	private String subscriberDetails;
	private String carehomeDetails;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getID() {
		return ID;
	}
	public void setID(String iD) {
		ID = iD;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public int getPort() {
		return port;
	}
	public void setPort(int port) {
		this.port = port;
	}
	public String getAuthenticationDetails() {
		return authenticationDetails;
	}
	public void setAuthenticationDetails(String authenticationDetails) {
		this.authenticationDetails = authenticationDetails;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getManufacturer() {
		return manufacturer;
	}
	public void setManufacturer(String manufacturer) {
		this.manufacturer = manufacturer;
	}
	public String getVersion() {
		return version;
	}
	public void setVersion(String version) {
		this.version = version;
	}
	public String getProtocol() {
		return protocol;
	}
	public void setProtocol(String protocol) {
		this.protocol = protocol;
	}
	public String getParameters() {
		return parameters;
	}
	public void setParameters(String parameters) {
		this.parameters = parameters;
	}
	public String getRegistrationFor() {
		return registrationFor;
	}
	public void setRegistrationFor(String registrationFor) {
		this.registrationFor = registrationFor;
	}
	public String getSubscriberDetails() {
		return subscriberDetails;
	}
	public void setSubscriberDetails(String subscriberDetails) {
		this.subscriberDetails = subscriberDetails;
	}
	public String getCarehomeDetails() {
		return carehomeDetails;
	}
	public void setCarehomeDetails(String carehomeDetails) {
		this.carehomeDetails = carehomeDetails;
	}
	
	
	
	
	
	
	
	
	

}
