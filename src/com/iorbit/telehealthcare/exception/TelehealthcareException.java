package com.iorbit.telehealthcare.exception;

public class TelehealthcareException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -2083733010120565236L;

	public TelehealthcareException(String message, Throwable cause) {
		super(message, cause);
	}

	public TelehealthcareException(String message) {
		super(message);
	}

	public TelehealthcareException(Throwable cause) {
		super(cause);
	}

}
